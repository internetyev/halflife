// Eval-reproduction temperature consistency guard: the sampling `temperature` the
// production role-analysis call sends must equal the `temperature` the L1.5b
// eval-reproduction procedure tells a human to set in the Anthropic workbench. If
// they drift, the eval reproduces a DIFFERENT call than production and its baseline
// no longer predicts shipped behaviour.
//
// The two sites:
//   1. `lib/anthropic/role-analysis.ts` — `const TEMPERATURE = 0`, wired into the
//      real `client.messages.create({ … temperature: TEMPERATURE … })` forced
//      tool-use call in `analyzeRole`. This is how production samples.
//   2. `evals/README.md` — Path A step 1 of the L1.5b procedure: "Pick
//      `claude-sonnet-4-6` as the model, temperature `0`, max tokens 2048." This is
//      what a human types into the Console workbench to reproduce the call and fill
//      `role-analysis-baseline.csv`.
//
// This closes the gap D-129 flagged and L5.100 / D-130 fixed. Before D-130, the
// production call passed NO `temperature` at all and inherited the SDK default of
// 1.0, while the README had pinned `temperature 0` since L1.5b — so the eval
// baseline was generated by deterministic sampling and production shipped
// stochastic sampling. Two runs of the same title could land in different score
// bands, and an eval row that passed at temp 0 said nothing about the call users
// actually get. Neither `next build` nor `tsc --noEmit` can see this: an ABSENT
// optional SDK field is valid TypeScript, and the README figure is prose.
//
// Hence the omission check below (`temperature:` must appear in the create payload)
// is as load-bearing as the equality check — the exact regression this guard exists
// to catch was a missing field, not a mismatched number. An equality check alone
// would have passed vacuously on the pre-D-130 code.
//
// Why NOT the sibling guards: `eval-reproduction-tokens-consistency` pins
// `max_tokens` and `model-id-consistency` pins the model id — both read this same
// README line, but neither reads the temperature figure between them.
//
// A single 0 anchor (EXPECTED_TEMPERATURE) pins the current source of truth so a
// coordinated roll across both surfaces is still a deliberate, test-visible edit
// here — the same drop-detector role EXPECTED_MAX_TOKENS plays in the L5.99 guard.
//
// Why a text guard: same D-080 wall as the L5.57–L5.99 arc — `role-analysis.ts`
// value-imports the `@/`-aliased `RoleAnalysisToolInput` and the `@anthropic-ai` SDK
// the bare `.mjs` node-test loader can't resolve and that aren't installed for the
// runner, and the README is prose. So it reads both as TEXT, strips comments from
// the `.ts` source, and regex-extracts each figure. Pure Node built-ins, no npm
// install — identical on the routine laptop and CI. Run via `npm test`.

import { test } from "node:test";
import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), "..", "..");

// The canonical source-of-truth sampling temperature. Named here as the
// drop-detector anchor: a coordinated roll across both surfaces would keep the
// cross-surface agreement check green, so this literal forces the roll to be a
// deliberate edit here too.
const EXPECTED_TEMPERATURE = 0;

function read(relPath) {
  return readFileSync(join(REPO_ROOT, relPath), "utf8");
}

// Strip `//` line comments and `/* */` block comments so a commented-out or
// documentation literal never satisfies (or breaks) an extractor. Good enough for
// this source — no `//` or `/*` appears inside a string literal we scan.
function stripComments(src) {
  return src.replace(/\/\*[\s\S]*?\*\//g, "").replace(/\/\/[^\n]*/g, "");
}

// (1) `const TEMPERATURE = <n>` — how the production call samples. Accepts a
// decimal so a future retune to e.g. 0.3 is read, not silently missed.
function codeTemperature(src) {
  const m = /const\s+TEMPERATURE\s*=\s*(\d+(?:\.\d+)?)\b/.exec(stripComments(src));
  return m ? Number(m[1]) : null;
}

// (2) `temperature <n>` from the eval README's Path A step 1.
function docTemperature(src) {
  const m = /temperature\s*`?(\d+(?:\.\d+)?)`?/i.exec(src);
  return m ? Number(m[1]) : null;
}

const sourceSrc = read("lib/anthropic/role-analysis.ts");
const readmeSrc = read("evals/README.md");

const code = codeTemperature(sourceSrc);
const doc = docTemperature(readmeSrc);

test("each temperature surface yields a number (vacuous-scan guard)", () => {
  // If either extractor silently returned null, the equality check below would pass
  // for the wrong reason (null === null). Assert both bit first.
  assert.ok(
    code !== null,
    "could not extract `const TEMPERATURE = <n>` from lib/anthropic/role-analysis.ts",
  );
  assert.ok(
    doc !== null,
    "could not extract `temperature <n>` from evals/README.md's Path A step 1",
  );
});

test("the TEMPERATURE const is actually wired into the messages.create call", () => {
  // THE regression this guard exists for. Omitting `temperature` is valid TypeScript
  // and silently inherits the SDK default (1.0) — production sampled stochastically
  // for the whole pre-D-130 life of the eval README. A const that never reaches the
  // create payload changes nothing about how production samples.
  assert.match(
    stripComments(sourceSrc),
    /temperature\s*:\s*TEMPERATURE\b/,
    "lib/anthropic/role-analysis.ts must pass `temperature: TEMPERATURE` into client.messages.create — " +
      "an omitted temperature falls back to the SDK default (1.0), not to the const",
  );
});

test("the eval README temperature equals the production call's (eval reproduces prod)", () => {
  // The load-bearing check: the human running the L1.5b eval must sample the way
  // production samples, or the baseline is generated against a call production does
  // not make and stops predicting shipped behaviour.
  assert.equal(
    doc,
    code,
    `evals/README.md says "temperature ${doc}" but the production call sends temperature=${code}; ` +
      `the eval would reproduce a different call than production — update both together`,
  );
});

test("both surfaces resolve to ONE shared temperature", () => {
  // Collapse to a single set: exactly one value may span the code const and the
  // documented figure.
  const all = new Set([code, doc]);
  assert.equal(
    all.size,
    1,
    `temperature is split across surfaces; found ${[...all].join(", ")} (code ${code}, doc ${doc})`,
  );
});

test("the shared temperature is the canonical 0 (drop-detector anchor)", () => {
  // Pin the current source of truth literally so a fully-coordinated roll — which the
  // agreement check alone would let through — is still a deliberate, test-visible edit here.
  assert.equal(
    code,
    EXPECTED_TEMPERATURE,
    `code TEMPERATURE must be ${EXPECTED_TEMPERATURE} (found ${code})`,
  );
  assert.equal(
    doc,
    EXPECTED_TEMPERATURE,
    `documented temperature must be ${EXPECTED_TEMPERATURE} (found ${doc})`,
  );
});
